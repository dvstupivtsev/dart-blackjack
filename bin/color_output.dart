String redColorString(String string) {
  return "\x1B[31m$string\x1B[0m";
}

String greenColorString(String string) {
  return "\x1B[32m$string\x1B[0m";
}

String yellowColorString(String string) {
  return "\x1B[33m$string\x1B[0m";
}

String blueColorString(String string) {
  return "\x1B[34m$string\x1B[0m";
}
